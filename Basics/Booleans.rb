# Booleans represent truth values and are typically used for control flow.
is_raining = true   # Boolean (True)
is_sunny = false    # Boolean (False)

#	Booleans are often the result of comparisons or logical operations:
5 > 3       # true
10 == 5     # false
!true       # false (negation)
true && false  # false (logical AND)
true || false  # true  (logical OR)